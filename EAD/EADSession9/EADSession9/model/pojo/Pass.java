package com.metacube.assignment.EADSession9.model.pojo;

public class Pass {
	private Integer regId;
	private String pricePlan;

	private Integer priceInINR;

	public Integer getRegId() {
		return regId;
	}

	public void setRegId(Integer regId) {
		this.regId = regId;
	}

	public String getPricePlan() {
		return pricePlan;
	}

	public void setPricePlan(String pricePlan) {
		this.pricePlan = pricePlan;
	}

	public Integer getPriceInINR() {
		return priceInINR;
	}

	public void setPriceInINR(Integer priceInINR) {
		this.priceInINR = priceInINR;
	}

}