package com.metacube.assignment.EADSession9.dto;

public class FriendProfile {
private Integer regId;
private String firstName;
private String lastName;
private String gender;
private String email;
private String contact;
private String vehchileName;
private String vehchileNumber;
private String vehchileType;

public Integer getRegId() {
	return regId;
}
public void setRegId(Integer regId) {
	this.regId = regId;
}
public String getFirstName() {
	return firstName;
}
public void setFirstName(String firstName) {
	this.firstName = firstName;
}
public String getLastName() {
	return lastName;
}
public void setLastName(String lastName) {
	this.lastName = lastName;
}
public String getGender() {
	return gender;
}
public void setGender(String gender) {
	this.gender = gender;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public String getContact() {
	return contact;
}
public void setContact(String contact) {
	this.contact = contact;
}
public String getVehchileName() {
	return vehchileName;
}
public void setVehchileName(String vehchileName) {
	this.vehchileName = vehchileName;
}
public String getVehchileNumber() {
	return vehchileNumber;
}
public void setVehchileNumber(String vehchileNumber) {
	this.vehchileNumber = vehchileNumber;
}
public String getVehchileType() {
	return vehchileType;
}
public void setVehchileType(String vehchileType) {
	this.vehchileType = vehchileType;
}

}
